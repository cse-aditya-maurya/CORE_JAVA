		/* Increment
		int i=10;
		int j=20;
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);
		int inc=++i - j++ + i++ - ++j;
		System.out.println("Content of inc is : "+inc);
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);

*/
		int i=34;
		int j=75;
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);	
