class Switch{
	public static void main(String[] args) {
		int day=9;
		switch(day){
		case 1:{
			System.out.println("Poha");
			break;
		}
		case 2:{
			System.out.println("Mess");
			break;
		}
		case 3:{
			System.out.println("Chawal");
			break;
		}
		case 4:{
			System.out.println("Maggi");
			break;
		}
		case 5:{
			System.out.println("biryani");
			break;
		}
		case 6:{
			System.out.println("dosa");
			break;
		}
		case 7:{
			System.out.println("thali");
			break;
		}
	default:{
			System.out.println("Paneer Bhurji");
		}
	}
	}
}